# команды на 1 раз
# git init - инициализирует гит
# git remote add origin ссылку на репозиторий - привязать ваш проект
# и ваш репозиторий
#
#
# команды которы пишутся всегда
# git add namefile\. - добавляет файлы для сохранения
# git commit -m 'name' - cохраняет файлы
# git push origin master\main - отправляет файлы в репозиторий
#
# git rm --cached namefile -r
#
#
# git clone link  - клонирует\коннектится с репозиторием
# git remote remove origin\namebranch - отвязывает проект от репозитория
#
#
# git branch - ветка
# git branch name - создание ветки
# git checkout name - переключение ветки